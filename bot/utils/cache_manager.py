class CacheManager:
    """Управляет локальными JSON-кэшами с начальными данными."""

    def __init__(self):
        self.cache_dir = config.CACHE_DIR
        self.cache_dir.mkdir(parents=True, exist_ok=True)
        self._init_seed()

    def _init_seed(self):
        """Инициализирует кэш начальными данными если его нет."""
        for key, data in _SEED.items():
            path = self._get_cache_path(key)
            if not path.exists():
                logger.info(f"Инициализирую кэш: {key}")
                self.save(key, data)

    def _get_cache_path(self, name: str) -> Path:
        return self.cache_dir / f"{name}.json"

    def load(self, name: str) -> Optional[dict]:
        path = self._get_cache_path(name)
        if not path.exists():
            if name in _SEED:
                return {"last_updated": datetime.utcnow().isoformat(), "data": _SEED[name]}
            return None
        try:
            with open(path, "r", encoding="utf-8") as f:
                return json.load(f)
        except Exception as e:
            logger.error(f"Ошибка загрузки кэша {name}: {e}")
            return None

    def save(self, name: str, data: dict) -> None:
        path = self._get_cache_path(name)
        cache_data = {
            "last_updated": datetime.utcnow().isoformat(),
            "data": data,
        }
        try:
            with open(path, "w", encoding="utf-8") as f:
                json.dump(cache_data, f, ensure_ascii=False, indent=2)
        except Exception as e:
            logger.error(f"Ошибка сохранения кэша {name}: {e}")

    def get(self, name: str) -> Optional[Any]:
        cache = self.load(name)
        return cache["data"] if cache else None

    def is_stale(self, name: str, max_age_minutes: int = 60) -> bool:
        cache = self.load(name)
        if not cache:
            return True
        last_updated = datetime.fromisoformat(cache["last_updated"])
        delta = datetime.utcnow() - last_updated
        return delta.total_seconds() > max_age_minutes * 60

    async def sync_all(self) -> bool:
        logger.info("Запуск синхронизации кэша...")
        return True


_cache_manager: Optional[CacheManager] = None


def get_cache_manager() -> CacheManager:
    global _cache_manager
    if _cache_manager is None:
        _cache_manager = CacheManager()
    return _cache_manager
