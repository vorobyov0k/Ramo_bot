def run():
    conn = sqlite3.connect(DB)
    c = conn.cursor()

    # 1. Показываем что удаляем
    c.execute("SELECT id, name FROM categories WHERE type='bar'")
    old_cats = c.fetchall()
    old_cat_ids = [r[0] for r in old_cats]
    c.execute(f"SELECT COUNT(*) FROM drinks WHERE category_id IN ({','.join('?'*len(old_cat_ids))})", old_cat_ids)
    old_count = c.fetchone()[0]
    print(f"Удаляю: {len(old_cats)} категорий, {old_count} напитков")
    for cat in old_cats:
        print(f"  - [{cat[0]}] {cat[1]}")

    # 2. Удаляем старые данные
    if old_cat_ids:
        c.execute(f"DELETE FROM drinks WHERE category_id IN ({','.join('?'*len(old_cat_ids))})", old_cat_ids)
        c.execute(f"DELETE FROM categories WHERE id IN ({','.join('?'*len(old_cat_ids))})", old_cat_ids)

    # 3. Вставляем новые категории
    cat_name_to_id = {}
    for name, sort_order in CATEGORIES:
        c.execute(
            "INSERT INTO categories (name, type, sort_order) VALUES (?, 'bar', ?)",
            (name, sort_order),
        )
        cat_name_to_id[name] = c.lastrowid

    # 4. Вставляем напитки
    for cat_name, drink_name, price, volume, is_non_alc, desc in DRINKS:
        cat_id = cat_name_to_id[cat_name]
        c.execute(
            """INSERT INTO drinks
               (category_id, name, price, volume_ml, is_non_alcoholic, description)
               VALUES (?, ?, ?, ?, ?, ?)""",
            (cat_id, drink_name, price, volume, int(is_non_alc), desc),
        )

    conn.commit()

    # 5. Итог
    c.execute("SELECT COUNT(*) FROM categories WHERE type='bar'")
    new_cats = c.fetchone()[0]
    c.execute("""SELECT COUNT(*) FROM drinks d
                 JOIN categories cat ON d.category_id=cat.id
                 WHERE cat.type='bar'""")
    new_drinks = c.fetchone()[0]
    conn.close()

    print(f"\nГотово: {new_cats} категорий, {new_drinks} напитков загружено.")
    print("⚠️  Фото к напиткам нужно загрузить заново через панель администратора.")


if __name__ == "__main__":
    run()
